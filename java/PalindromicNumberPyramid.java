import java.util.*;
public class PalindromicNumberPyramid {
    public static void main(String[] args) {
        int n = 5;
        //outer loop
        for(int i = 1; i <= n; i++){
        for(int j = 1; j <= n - i; j++){
            System.out.print("  ");

        }
        //inner loop
        //increasing order
        for(int j = 1; j <= i; j++){
            System.out.print(j+" ");
        }
            // decreasing order
            for(int j = i - 1; j >= 1; j--){
                System.out.print(j+" ");  
            }
            System.out.println();
        
      }
    }
    
}
   








































































































































































































































































































































































































































































































































































